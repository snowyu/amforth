# -----------------------------------------------------------------------------
#  Multiplication, Division and Remainder available as opcodes in RV32IM
#  Rewrite these to get a pure RV32I Forth.
# -----------------------------------------------------------------------------

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "*", STAR # ( x1 x2 -- x1*x2 )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  addi x4, x4, 4
  mul x3, x5, x3
  NEXT

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "um*", UMSTAR # ( u1 u2 -- ud )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  mul x6, x5, x3
  sw x6, 0(x4)
  mulhu x3, x5, x3
  NEXT

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "m*", MSTAR # ( n1 n2 -- d )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  mul x6, x5, x3
  sw x6, 0(x4)
  mulh x3, x5, x3
  NEXT

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "/", SLASH # ( n1 n2 -- n1/n2 )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  addi x4, x4, 4
  div x3, x5, x3
  NEXT

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "mod", MOD # ( n1 n2 -- n1%n2 )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  addi x4, x4, 4
  rem x3, x5, x3
  NEXT

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "u/mod", U_SLASH_MOD # ( u1 u2 -- rem quot )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  remu x6, x5, x3
  sw x6, 0(x4)
  divu x3, x5, x3
  NEXT

# -----------------------------------------------------------------------------
  CODEWORD Flag_visible, "/mod", SLASHMOD # ( n1 n2 -- rem quot )
# -----------------------------------------------------------------------------
  lw x5, 0(x4)
  rem x6, x5, x3
  sw x6, 0(x4)
  div x3, x5, x3
  NEXT
 
